# Python program to find maximum and the minimum value in a set
# Python code to get the maximum element from a set
def MAX(sets):
    return (max(sets))


# Driver Code
sets = set([8, 16, 24, 1, 25, 3, 10, 65, 55])
print(sets)
print("Max value:", MAX(sets))


# Python code to get the minimum element from a set
def MIN(sets):
    return min(sets)


# Driver Code
print("Min value:", MIN(sets))
