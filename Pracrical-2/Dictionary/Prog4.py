# Python script to add a key to a dictionary.
# Sample Dictionary : {0: 10, 1: 20}
# Expected Result : {0: 10, 1: 20, 2: 30}
dict1 = {
    0: 10,
    1: 20,
}
print("Dictionary before updation:", dict1)

# adds a key to the dictionary
dict1.update({2: 30})
print("Dictionary after updation:", dict1)




